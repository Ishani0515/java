class SpChar
{
   public static void main(String args[])
   {               
      String name="Hello";
       System.out.print(name.charAt(0)+" ");
       System.out.print(name.charAt(1)+" ");
       System.out.print(name.charAt(2)+" ");
       System.out.print(name.charAt(3)+" ");
       System.out.print(name.charAt(4)+" ");
 
   }
}